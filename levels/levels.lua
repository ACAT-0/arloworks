function loadLevel(level, size)
    local outLevel = {}
    for i, row in pairs(level) do
        for v, tile in pairs(row) do
            if (tile ~= 0) then
                table.insert(outLevel,
                    CollisionEntity(vector.new((v * size) - size, (i * size) - size), vector.new(size, size)))
            end
        end
    end
    return outLevel
end

function pushLevel(level, buffer)
    for i, tile in pairs(level) do
        tile:addToBuffer(buffer)
    end
end
